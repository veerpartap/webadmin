        return $this->created;
    }

    /**
     * Set updated
     *
     * @param \DateTime
     * @return User
     */
    public function setUpdated()
    {
        if(!isset($this->updated))
            $this->updated = new \DateTime();

        #$this->updated = $updated;
    
        return $this;
    }

