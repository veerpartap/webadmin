     */
    public function setUserprofile(\Site\Bundle\UserBundle\Entity\UserProfile $userprofile = null)
    {
        $this->userprofile = $userprofile;
    
        return $this;
    }

    /**
     * Get userprofile
     *
     * @return \Site\Bundle\UserBundle\Entity\UserProfile 
